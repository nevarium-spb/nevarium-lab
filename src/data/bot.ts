// Простейший роутер свободного ввода по ключевым словам
const keywordRoutes: [RegExp, string][] = [
  [/(цен|стоим|стоит|тариф|прайс|сколько|бюджет)/i, 'pricing'],
  [/(услуг|что.*(умеете|делаете)|направлен)/i, 'services'],
  [/(ассистент|gpt|джипити)/i, 'svc_assistants'],
  [/(бот|чат-бот|чатбот)/i, 'svc_bots'],
  [/(контент|пост|статья|smm|смм|маркетинг|reels|рилс)/i, 'svc_content'],
  [/(начать|как.*(работаете|происходит)|этап|процесс|аудит)/i, 'how'],
  [/(заявк|связ|звон|менеджер|консульт|эксперт|привет|здравств)/i, 'start'],
]

export function routeFreeText(text: string): string {
  for (const [re, node] of keywordRoutes) {
    if (re.test(text)) return node
  }
  return 'fallback'
}
